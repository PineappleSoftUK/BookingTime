// will run if create asset form was submitted
$(document).on('submit', '#create-asset-form', function(){

  var timeslotsObj = {
    Monday: [],
    Tuesday: [],
    Wednesday: [],
    Thursday: [],
    Friday: [],
    Saturday: [],
    Sunday: []
  };

  //This will loop through the checkboxes and add selected values to timeslotJSON
  $(":checkbox").each(function() {
    if ($(this).is(':checked')) {
      timeslotsObj[$(this).prop('className')].push($(this).val()); //Get checkbox class name then push the value to that index
    }
  });

  //Now add this array to the form 'post' data by way of a hidden element:
  var input = $("<input>")
    .attr("type", "hidden")
    .attr("name", "timeslots").val(JSON.stringify(timeslotsObj));
  $('#create-asset-form').append(input);

  // get form data
  var form_data=JSON.stringify($(this).serializeObject());

  // submit form data to api
  $.ajax({
    url: apiPath + "api/asset/create.php",
    type : "POST",
    contentType : 'application/json',
    //send jwt header
    headers: {
      Authorization: 'Bearer ' + getCookie('jwt')
    },
    data : form_data,
    success : function(result) {
      // asset was created, go back to assets list
      var alertMessage = encodeURIComponent("Asset successfully added");
      window.location.href = "index.html?alert_type=success&alert_text=" + alertMessage;
    },
    error: function(xhr, resp, text) {
      // show error to console
      var alertMessage = encodeURIComponent("Create asset failed: " + xhr.responseJSON.message);
      window.location.href = "index.html?alert_type=error&alert_text=" + alertMessage;
    }
  });
  return false;
});
